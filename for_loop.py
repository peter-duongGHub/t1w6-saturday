# print(range (5, 10, 1))

# For loop in a range
for i in range(10, 5, -1):
    print(i)

# For loop in a list
fruits = ["Apple", "Banana", "Cherry"]

for i in fruits:
    print(i)

for i in "CoderAcademy":
    print(i)

for i in range(len(fruits)):
    print(f"Index {i}: {fruits[i]}")

